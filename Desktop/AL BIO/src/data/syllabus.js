// Get total topic count for a subject
export function getTotalTopics(subject) {
    const subjectData = syllabus[subject];
    if (!subjectData) return 0;

    return subjectData.units.reduce((total, unit) => total + unit.topics.length, 0);
}

// Get unit by ID
export function getUnitById(subject, unitId) {
    const subjectData = syllabus[subject];
    if (!subjectData) return null;

    return subjectData.units.find(unit => unit.id === unitId);
}

// Get topic by ID
export function getTopicById(subject, topicId) {
    const subjectData = syllabus[subject];
    if (!subjectData) return null;

    for (const unit of subjectData.units) {
        const topic = unit.topics.find(t => t.id === topicId);
        if (topic) return topic;
    }

    return null;
}
